## Turtles!

import turtle

window = turtle
window = turtle.Screen() # Set up the window and it's attributes.
window.bgcolor("blue")

# TMNT - turtles
mike = turtle
mike.color("blue")
mike.turtlesize(20)
mike.shape("turtle")

# move mike around
mike.speed(2)
mike.penup()
mike.forward(1000)
mike.left(90)


window.exitonclick()


